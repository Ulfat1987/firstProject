package practice;

public class Increment_Decrement {
    public static void main(String[] args) {
        int a = 12;
        int b = a++;
        System.out.println(b);
        System.out.println(a);

        int x = 15;
        int y = x--;
        System.out.println(x);
        System.out.println(y);
    }
}
